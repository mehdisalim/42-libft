#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*src;

	i = 0;
	src = (char *)b;
	while (src[i] != 0 && i < len)
	{
		src[i] = c;
		i++;
	}
	return (src);
}
